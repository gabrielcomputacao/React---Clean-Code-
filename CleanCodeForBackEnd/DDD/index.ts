// DDD = desgin dirigido a dominio

/* 
    Prática de pensar como vai fazer o seu software

    Desenho da aplicação, momento de escopo do projeto

    Dominio é o problema que voce está resolvendo e seu contexto

    Exemplo: 
        E-commerce

            O ambiente e os problemas são chamados de dominio, não é algo que vai virar algo dentro do codigo
            Dominio é tudo que entra nos problemas a serem resolvidos com relação ao ramo ou atividade do software

            Olha para dentro do projeto e dividi em subdominios

            Subdominio do e=commerce
            -> DIferentes areas com pequenos entendimentos menores sobre o projeto maior, Exemplo:
                - Logística
                - Faturamento
                - Pagamento
                - Estoque

    Domain Experts
        Pessoa que sabe tudo sobre o dominio

    Entidades - Estruturas ou atores dentro de cada subdominio
    Casos de uso - ações dentro do subdominio

*/
